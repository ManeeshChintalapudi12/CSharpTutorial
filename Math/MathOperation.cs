public class MathOperation
{
  public void Maximum()
  {
    int a = 7;
    int b = 12;
    Console.WriteLine(Math.Max(a, b));
  }

  public void Minimum()
  {
    int a = 7;
    int b = 12;
    Console.WriteLine(Math.Min(a, b));
  }

  public void SquareRoot()
  {
    int a = 7;
    Console.WriteLine(Math.Sqrt(a));
  }
    
  public void Figure()
    {
      float b = 7.22F;
      Console.WriteLine(Math.Round(b));
    }
}