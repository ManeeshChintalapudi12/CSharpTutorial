public class DataTypes
{
    public void Integer()
    {
        int myNum = 20;
        Console.WriteLine(myNum);
    }

    public void Long()
    {
        long myNum = 15000000000L;
        Console.WriteLine(myNum);
    }

    public void Float()
    {
        float myNum = 5.75F;
        Console.WriteLine(myNum);
    }

    public void Double()
    {
        double myNum = 19.99D;
        Console.WriteLine(myNum);
    }

    public void Char()
    {
        char myGrade = 'B';
        Console.WriteLine(myGrade);
    }

    public void String()
    {
        string greeting = "Hello World";
        Console.WriteLine(greeting);
    }
    
     public void Boolean()
    {
        bool isCSharpFun = true;
        Console.WriteLine(isCSharpFun);
    }
}